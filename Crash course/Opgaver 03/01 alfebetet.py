alphabet = "abcdefghijklmnopqrstuvwxyzæøå"

# 1. der udtages de første 10 bogstaver. 
print(alphabet[:10])
# 2. der udtages 3. 4. og 5. bostav.
print(alphabet[2:5])
# 3. der udtages 3-6 bostaver.
print(alphabet[2:7])
# 4. der udtages sidste bogstav.
print(alphabet[-1])
# 5. der udtages alt på nær de første 3.
print(alphabet[3:-1])
# 6. de sidste 3 udtages.
print(alphabet[-3:])
# 7. alt på nær de sidste 3 printes.
print(alphabet[:-3])